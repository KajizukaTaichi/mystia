pub trait Node {
    fn compile(&self, ctx: &mut Compiler) -> Option<String>;
    fn type_infer(&self, ctx: &mut Compiler) -> Option<Type>;
    fn parse(source: &str) -> Option<Self>
    where
        Self: Node + Sized;
}

#[derive(Debug, Clone)]
pub struct Compiler {
    index: i32,
    data: Vec<String>,
    array: Vec<String>,
    declare: Vec<String>,
    variable: HashMap<String, Type>,
    function: HashMap<String, (Vec<Type>, Type)>,
    argument: HashMap<String, Type>,
}

impl Compiler {
    pub fn new() -> Self {
        Compiler {
            index: 0,
            data: vec![],
            array: vec![],
            declare: vec![],
            variable: HashMap::new(),
            function: HashMap::new(),
            argument: HashMap::new(),
        }
    }

    pub fn build(&mut self, source: &str) -> Option<String> {
        let ast = Block::parse(source)?;
        let ret = ast.type_infer(self)?;
        Some(format!(
            "{5}\n(module (memory $mem 1) {2} {3} (func (export \"_start\") {1} {4} {0}))",
            ast.compile(self)?,
            config_return!(ret, self)?,
            join!(self.data),
            join!(self.declare),
            expand_local(self)?,
            format!(
                ";; Generated by Mystia compiler on {}",
                Local::now().format("%Y/%m/%d %H:%M:%S")
            )
        ))
    }
}
