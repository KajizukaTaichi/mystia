pub trait Node {
    fn compile(&self, ctx: &mut Compiler) -> Option<String>;
    fn type_infer(&self, ctx: &mut Compiler) -> Option<Type>;
    fn parse(source: &str) -> Option<Self>
    where
        Self: Node + Sized;
}

#[derive(Debug, Clone)]
pub struct Compiler {
    /// メモリ配置用のアドレス
    alloc_index: i32,
    /// 静的文字列データ
    static_data: Vec<String>,
    /// 関数定義コードの集合
    declare_code: Vec<String>,
    /// 型推論：変数の名前と型
    variable_type: HashMap<String, Type>,
    /// 型推論：関数の名前と引数と戻り値の型
    function_type: HashMap<String, (Vec<Type>, Type)>,
    /// 型推論：引数の名前と型
    argument_type: HashMap<String, Type>,
}

impl Compiler {
    pub fn new() -> Self {
        Compiler {
            alloc_index: 0,
            static_data: vec![],
            declare_code: vec![],
            variable_type: HashMap::new(),
            function_type: HashMap::new(),
            argument_type: HashMap::new(),
        }
    }

    pub fn build(&mut self, source: &str) -> Option<String> {
        let ast = Block::parse(source)?;
        let ret = ast.type_infer(self)?;
        Some(format!(
            "{5}\n(module (memory $mem 1) {2} {3} (func (export \"_start\") {1} {4} {0}))",
            ast.compile(self)?,
            config_return!(ret, self)?,
            join!(self.static_data),
            join!(self.declare_code),
            expand_local(self)?,
            format!(
                ";; Generated by Mystia compiler on {}\n;; Source Hash: {:x}",
                Local::now().format("%Y/%m/%d %H:%M:%S"),
                {
                    let mut hasher = Sha256::new();
                    hasher.update(source.as_bytes());
                    hasher.finalize()
                }
            )
        ))
    }
}
